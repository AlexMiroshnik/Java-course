package com.company;

public class Calls {
    public static void main(String[] args) {
        int c1 = 3;
        int c2 = 6;
        int c3 = 16;
        int t1 = 20;
        int t2 = 40;
        int t3 = 50;
        System.out.println(c1 * t1);
        System.out.println(c2 * t2);
        System.out.println(c3 * t3);
        System.out.println(c1 * t1 + c2 * t2 + c3 * t3);

    }
}

